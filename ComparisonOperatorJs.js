const id = '100';

//Equal To
if (id == 100) {
  console.log('Correct');
} else {
  console.log('InCorrect');
}


// Not Equal To

if (id != 100) {
  console.log('Correct');
} else {
  console.log('InCorrect')
}

// EQUAL TO VALUE AND TYPE - UseMost of Time

if (id === 100) {
  console.log('Correct');
} else {
  console.log('InCorrect');
}


if (id !== 100) {
  console.log('Correct');
} else {
  console.log('InCorrect')
}